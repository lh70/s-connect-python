

class AbstractSensor:

    """
    Holds the minimum requirement a sensor has to fulfil

    leases are stored on sensor and handled by the SensorManager
    value holds the most recent value generated by the sensor
     -> to be updated by the sensor which must implement an update() method
    """
    def __init__(self):
        self.leases = 0
        self.value = 0

    """
    Returns the most recently updated sensor value
    
    General convention to use a method rather than to expose the value directly
    """
    def get(self):
        return self.value
